package com.b2b.common.domain;

import java.util.Date;

public class ShopItemStock extends ShopItemStockBase{
    private Integer id;

    private Integer shopId;

    private Integer itemId;

    private Integer num;

    private Long salePrice;

    private Long sourcingPrice;

    private Date sumDate;

    private Date createdTime;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getShopId() {
        return shopId;
    }

    public void setShopId(Integer shopId) {
        this.shopId = shopId;
    }

    public Integer getItemId() {
        return itemId;
    }

    public void setItemId(Integer itemId) {
        this.itemId = itemId;
    }

    public Integer getNum() {
        return num;
    }

    public void setNum(Integer num) {
        this.num = num;
    }

    public Long getSalePrice() {
        return salePrice;
    }

    public void setSalePrice(Long salePrice) {
        this.salePrice = salePrice;
    }

    public Long getSourcingPrice() {
        return sourcingPrice;
    }

    public void setSourcingPrice(Long sourcingPrice) {
        this.sourcingPrice = sourcingPrice;
    }

    public Date getSumDate() {
        return sumDate;
    }

    public void setSumDate(Date sumDate) {
        this.sumDate = sumDate;
    }

    public Date getCreatedTime() {
        return createdTime;
    }

    public void setCreatedTime(Date createdTime) {
        this.createdTime = createdTime;
    }
}